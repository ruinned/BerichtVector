/***********************************************************************************************************************
  Name:         EcuM_GeneratorCompatibilityError
  Parameter:    
  Return value: 
  Description:  
***********************************************************************************************************************/
void EcuM_GeneratorCompatibilityError(uint16 moduleIf, uint8 error)
{
  tBteEvent evnt;

  BteLog_Event_InitExt(&evnt, kTscEcuM_CI, kTscEcuM_GeneratorCompatibilityError, "EcuM_GeneratorCompatibilityError");
  evnt.data1 = (uint8)(moduleIf > 8);
  evnt.data2 = (uint8)(moduleIf & 0xFF);
  evnt.data3 = error;
  
  evnt.numParam = 3;
#if defined USE_PRINTF
  (void)sprintf( evnt.text_param, "(module=%d, error=%d)", moduleIf, error );
#endif
  BteLog_Event_Log( &evnt );
}


